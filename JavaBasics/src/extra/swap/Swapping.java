package extra.swap;

public class Swapping {
    public static void main(String[] args) {

        int a = 5;
        int b = 10;

        System.out.println(a);
        System.out.println(b);

        int temp;
        temp = a;
        a = b;
        b = temp;

        System.out.println(a);
        System.out.println(b);

    }
}
